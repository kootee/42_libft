#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;
	size_t	i;

	i = 0;
	substr = malloc(len * sizeof(char));
	while (i < len && s[i])
		substr[i] = s[start + i];
	return(substr);
}